package com.ems.entity;

public class Address {
private String counterA;
private String rack1;
public Address(String counterA, String rack1) {
	super();
	this.counterA = counterA;
	this.rack1 = rack1;
}
public String getCounterA() {
	return counterA;
}
public String getRack1() {
	return rack1;
}
}

